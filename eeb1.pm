// eeb model
dtmc

module eeb

	// local state
	s : [0..25] init 0;
	// value of the dice
	d : [0..4] init 0;
	
	// for a0, a1, a2, a3
	[] s=0 -> 0.25 : (s'=1) + 0.25 : (s'=2) + 0.25 : (s'=3) + 0.25 : (s'=4);

	// for a0 , a01, a02, a03 with b00, b01, b02, b03
	[] s=1 -> 0.25 : (s'=5) + 0.25 : (s'=6) + 0.25 : (s'=7) + 0.25 : (s'=8);
	[] s=2 -> 0.25 : (s'=9) + 0.25 : (s'=10) + 0.25 : (s'=11) + 0.25 : (s'=12);
	[] s=3 -> 0.25 : (s'=13) + 0.25 : (s'=14) + 0.25 : (s'=15) + 0.25 : (s'=16);
	[] s=4 -> 0.25 : (s'=17) + 0.25 : (s'=18) + 0.25 : (s'=19) + 0.25 : (s'=20);

	// for t1, t2, t3, t4
	[] s=5 -> 1 : (s'=21);
	[] s=6 -> 1 : (s'=21);
	[] s=7 -> 1 : (s'=21);
	[] s=8 -> 1 : (s'=21);
	[] s=9 -> 1 : (s'=22);
	[] s=10 -> 1 : (s'=22);
	[] s=11 -> 1 : (s'=22);
	[] s=12 -> 1 : (s'=22);
	[] s=13 -> 1 : (s'=23);
	[] s=14 -> 1 : (s'=23);
	[] s=15 -> 1 : (s'=23);
	[] s=16 -> 1 : (s'=23);
	[] s=17 -> 1 : (s'=24);
	[] s=18 -> 1 : (s'=24);
	[] s=19 -> 1 : (s'=24);
	[] s=20 -> 1 : (s'=24);

	// for s25
	[] s=21 ->  1 : (s'=25) & (d'=1);
	[] s=22 ->  1 : (s'=25) & (d'=2);
	[] s=23 ->  1 : (s'=25) & (d'=3);
	[] s=24 ->  1 : (s'=25) & (d'=4);

	// final state
	[] s=25 -> (s'=25);
	
endmodule

rewards "eeb_wins"
	[] s<25 : 1;
endrewards
